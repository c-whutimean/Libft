#include "libft.h"

int	b_negcheck(int n)
{
	if (n < 0)
	{
		if (n == -2147483647)
		{
			ft_putstr("-2147483647\n");
			return (n);
		}
		n = n * -1;
		ft_putstr("-");
	}
	return (n);
}
