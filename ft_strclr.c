#include "libft.h"

void	ft_strclr(char *s)
{
	int		i;
	int		mod;

	i = 0;
	mod = 0;
	while (s[i] != '\0')
		i++;
	while (mod <= i)
	{
		s[mod] = '\0';
		mod++;
	}
}
